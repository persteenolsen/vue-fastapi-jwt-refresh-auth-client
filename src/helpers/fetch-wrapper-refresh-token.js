import { useAuthStore } from '@/stores';

export const fetchWrapperRefreshToken = {
    get: request('GET'),
    post: request('POST'),
    put: request('PUT'),
    delete: request('DELETE')
};


function request(method) {
    return (url, body, { credentials } = {}) => {
        const requestOptions = {
            method,
            headers: authHeader(url)
        };
        if (body) {
            
            // 26-01-2026 - Get the refreshToken from state set in auth.store
            const { rToken } = useAuthStore();

            requestOptions.headers['Content-Type'] = 'application/json';
               
            // 26-01-2026 - Debugging
            console.log( 'fetch.wrapper-refresh-token.js - Refresh Token: ' + rToken );
            
            // 26-01-2026 - The refresh-token-spa at the API only accepts a String RefreshToken
            requestOptions.body = JSON.stringify( rToken );

            // 26-01-2026 - Simulation of Refresh timer flow by a static RefreshToken generated by the OpenAPI 
            //requestOptions.body = JSON.stringify("eyJhbGciOiJIUzI1NiIsInR5cCI6IkpXVCJ9.eyJzdWIiOiJ0ZXN0dXNlciIsImV4cCI6MTc2OTM3OTI2NH0.DY1xGp-                  //RTSBkqUKEmZC7hsdSH8md7mNNpVSKVzLpEac");
            //requestOptions.body = JSON.stringify(body);
        }
        if (credentials) {
            requestOptions.credentials = credentials;
        }
        return fetch(url, requestOptions).then(handleResponse);
    }
}


// helper functions

function authHeader(url) {

    // return auth header with jwt if user is logged in and request is to the api url
    const { user } = useAuthStore();

    const isLoggedIn = !!user?.jwtToken;

    const isApiUrl = url.startsWith(import.meta.env.VITE_API_URL);
    if (isLoggedIn && isApiUrl) {
        return { Authorization: `Bearer ${user.jwtToken}` };
    } else {
        return {};
    }
}

function handleResponse(response) {
    return response.text().then(text => {
        const data = text && JSON.parse(text);
        
        // 29-12-2025 - Log status code for debugging
        console.log('fetch.wrapper-refresh-token.js - Statuscode by response.status: ' + response.status);

        if (!response.ok) {

            const { user, logout } = useAuthStore();

            if ([401, 403].includes(response.status) && user) {
                
                // auto logout if 401 Unauthorized or 403 Forbidden response returned from api
                // Logout message for debugging
                console.log('fetch.wrapper-refresh-token.js - Auto logout due to 401/403 response');
                logout();
            }
            
            // 28-12-2025 - Modified error handling to show status code if User Credentials are wrong
            //const error = (data && data.message) || response.statusText;
            //const error = (data && data.message) || response.statusText || response.status;
            
            let error = "";
            
            // 25-01-2026 - Modified error handling to validate status code if User Credentials are wrong
            //if( response.status.equals('')) 
            //    error = (data && data.message) || response.statusText;
            //else
            error = (data && data.message) || response.statusText || response.status;
            
            // Debugging logs for error status
            console.log('fetch.wrapper-refresh-token.js - Statuscode by response.statusText: ' + response.statusText);
           
            return Promise.reject(error);
        }

        return data;
    });
}    
